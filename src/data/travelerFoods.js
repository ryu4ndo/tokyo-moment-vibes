const foodById = new Map(TRAVELER_FOODS.map((f) => [f.id, f]));

export function getTravelerFood(id) {
  return foodById.get(id);
}

export function getTravelerFoodLabel(food, locale = 'ja') {
  if (!food) return '';
  return locale === 'en' ? food.labelEn : food.labelJa;
}

export function getTravelerFoodField(food, field, locale = 'ja') {
  if (!food) return '';
  const key = `${field}${locale === 'en' ? 'En' : 'Ja'}`;
  return food[key] ?? '';
}
